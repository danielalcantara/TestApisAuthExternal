package br.com.cea.model;

import java.io.Serializable;
import java.util.Date;

import br.com.cea.dao.logger.ReflectionLogger;

public class PedidoSkuModel implements Serializable, ReflectionLogger {

	private static final long serialVersionUID = 271414405696724980L;
	private Long codigoFornecedor;
	private String nuNfItem;
	private Date dataEmsNf;
	private String cdItem;
	private Long niLocEtg;
	
	public Long getCodigoFornecedor() {
		return codigoFornecedor;
	}
	public void setCodigoFornecedor(Long codigoFornecedor) {
		this.codigoFornecedor = codigoFornecedor;
	}
	public String getNuNfItem() {
		return nuNfItem;
	}
	public void setNuNfItem(String nuNfItem) {
		this.nuNfItem = nuNfItem;
	}
	public Date getDataEmsNf() {
		return dataEmsNf;
	}
	public void setDataEmsNf(Date dataEmsNf) {
		this.dataEmsNf = dataEmsNf;
	}
	public String getCdItem() {
		return cdItem;
	}
	public void setCdItem(String cdItem) {
		this.cdItem = cdItem;
	}
	public Long getNiLocEtg() {
		return niLocEtg;
	}
	public void setNiLocEtg(Long niLocEtg) {
		this.niLocEtg = niLocEtg;
	}
	@Override
	public String toString() {
		return "PedidoSkuModel [codigoFornecedor=" + codigoFornecedor + ", nuNfItem=" + nuNfItem + ", dataEmsNf="
				+ dataEmsNf + ", cdItem=" + cdItem + ", niLocEtg=" + niLocEtg + "]";
	}
	
	
	
}
